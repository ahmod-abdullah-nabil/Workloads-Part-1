# Problem Name: A program to determine the average number of some inputs.
numbers = [0, 1, 2, 3]
numbers[0] = input("Please enter a number: ")
numbers[1] = input("Please enter a second number: ")
numbers[2] = input("Please enter a third number: ")
numbers[3] = input("Please enter a forth number: ")
average = (int(numbers[0]) + int(numbers[1]) +
           int(numbers[2]) + int(numbers[3]) / 4)
print("Average value is= ", average)
